package intro_to_java;
public class MadLibs1 {
    public static void main(String[] args) {
        // Fill these in with silly words/strings
        String pluralnoun1 = "unicorns";
        String pluralnoun2 = "puppies";
        String color1 = "purple";
        String color2 = "green";
        String food = "pizza";

        // Run to see the silly poem!
        System.out.println("Roses are " + color1);
        System.out.println(pluralnoun1 + " are " + color2);
        System.out.println("I like " + food);
        System.out.println("Do " + pluralnoun2 + " like them too?");
        
        // Now come up with your own silly poem!
        System.out.println("Flying " + pluralnoun1 + " in the " + color2 + " sky,");
        System.out.println("Eating " + food + " as the " + pluralnoun2 + " fly by.");
        System.out.println("With " + color1 + " rainbows and " + color2 + " trees,");
        System.out.println("Life's a wacky breeze for you and me!");
    }
}

